package com.startjava.lesson_1.variable.Variable;

public class Variable {
    public static void main(String[] args) {
        byte memory = 8;
        short cycleCount = 17;
        int capacity = 256;
        long justMaxLongNumber = 9223372036854775807L;
        float systemFirmwareVersion = 7429.415f;
        double osLoaderVersion = 7429.415;
        char justCharacter = '@';
        boolean activationLockStatus = true;

        System.out.println(memory);
        System.out.println(cycleCount);
        System.out.println(capacity);
        System.out.println(justMaxLongNumber);
        System.out.println(systemFirmwareVersion);
        System.out.println(osLoaderVersion);
        System.out.println(justCharacter);
        System.out.println(activationLockStatus);
    }
}